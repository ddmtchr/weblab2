public class ResponseObject {
    private String result;
    private String x;
    private String y;
    private String r;
    private String execTime;
    private String currentTime;

    public ResponseObject(String result, String x, String y, String r, String execTime, String currentTime) {
        this.result = result;
        this.x = x;
        this.y = y;
        this.r = r;
        this.execTime = execTime;
        this.currentTime = currentTime;
    }
}
